#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	zero;
	char	*c;

	c = (char *)s;
	if (n <= 0)
		return ;
	zero = 0;
	while (n-- > 0)
		*(c++) = 0;
}
